package day0601.collection;
import java.util.ArrayList;
import java.util.Collections;

class ArrayListEx1 {
	public static void main(String[] args) {
		ArrayList<Integer> list1 = new ArrayList<Integer>(10);
		list1.add(new Integer(5)); // 0
		list1.add(new Integer(4)); // 1
		list1.add(new Integer(2)); // 2
		list1.add(new Integer(0)); // 3
		list1.add(new Integer(1)); // 4
		list1.add(new Integer(3)); // 5

		// list1.get(0);

		ArrayList list2 = new ArrayList(list1.subList(1, 4)); // 4,2,0
		print(list1, list2);

		Collections.sort(list1); // list1과 list2를 정렬한다.
		Collections.sort(list2); // Collections.sort(List l)
		print(list1, list2);

		System.out.println("list1.containsAll(list2):" + list1.containsAll(list2));

		// 4,2,0,AA,B,C
		list2.add("B");
		list2.add("C");
		list2.add(3, "A");
		print(list1, list2);

		list2.set(3, "AA");
		print(list1, list2);

		// list1에서 list2와 겹치는 부분만 남기고 나머지는 삭제한다.
		System.out.println("list1.retainAll(list2):" + list1.retainAll(list2));
		print(list1, list2);

		// list2에서 list1에 포함된 객체들을 삭제한다.
		for (int i = list2.size() - 1; i >= 0; i--) {
			if (list1.contains(list2.get(i))) //해당 리스트(컬렉션)에서 값을 꺼내올대는 get로 선언하면 된다.
				list2.remove(i);
		}
		print(list1, list2);
	} // main

	static void print(ArrayList list1, ArrayList list2) {
		System.out.println("list1:" + list1);
		System.out.println("list2:" + list2);
		System.out.println();
	}
} // class

