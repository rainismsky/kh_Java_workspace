package exam;

import java.util.*;
import java.lang.*;
import java.io.*;

class CodingEveryBody {

	public static void main(String[] args) throws java.lang.Exception 
	{
		int a; //변수(변할수있는수) a는 정수형(int)이다 
		a = 1; //a라는 공간에 1을 대입한다(담는다) => a안엔 1이존재
		System.out.println(a+1); //2  
		
		a = 2;
		System.out.println(a+1); //3
		
		

	}
	}

